     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $data = array(
            'product_id' => $request->product_id,
            'qty' => $request->qty,
            'user_id' => Auth::user()->id,


        );
        Cart::create($data);
        return redirect()->route('cart');

